    //Rohit Salunkhe
private String initiateMultipartUpload(String bucketName, String keyPath) {

    CreateMultipartUploadRequest request = CreateMultipartUploadRequest.builder()
            .bucket(bucketName)
            .key(keyPath)
            //Rohit Salunkhe - Checksum algorithm is set to SHA256 for better integrity verification, can be changed to MD5 if needed
            // .checksumAlgorithm(ChecksumAlgorithm.SHA256)
            //Rohit Salunkhe - Checksum algorithm is set to MD5 for compatibility with existing systems, can be changed to SHA256 if needed
            .build();

    try {

        LOG.info("Initiating multipart upload. Bucket={}, Key={}", bucketName, keyPath);

        CreateMultipartUploadResponse response =
                s3Client.createMultipartUpload(request);

        if (response == null || response.uploadId() == null) {
            throw new S3Exception(
                    "S3 returned null response or uploadId.",
                    new NullPointerException("uploadId is null"));
        }

        LOG.info("Multipart upload initiated successfully. UploadId={}",
                response.uploadId());

        return response.uploadId();

    } catch (software.amazon.awssdk.services.s3.model.S3Exception e) {

        LOG.error("AWS S3 exception during multipart upload. StatusCode={}, AWSMessage={}",
                e.statusCode(),
                e.awsErrorDetails() != null
                        ? e.awsErrorDetails().errorMessage()
                        : e.getMessage(),
                e);

        throw new S3Exception("Failed to initiate multipart upload to S3.", e);

    } catch (SdkServiceException e) {

        LOG.error("S3 service exception: {}", e.getMessage(), e);

        throw new S3Exception(
                "S3 service is unavailable. Please try again later.",
                e);

    } catch (Exception e) {

        LOG.error("Unexpected error during multipart upload", e);

        throw new S3Exception(
                "Unexpected error during multipart upload.",
                e);
    }
}

